export function getCodeSample(algorithm: string): AlgorithmCode | undefined {
  return codeSamples.find((s) => s.algorithm === algorithm);
}

export function getLineNumbers(
  sample: AlgorithmCode,
  lineKey: string
): number[] {
  return sample.lineMap[lineKey] ?? [];
}
